//Printing Number from 1 to 10 with all loop;
//for loop
for( let i=1;i<=10;i++){
    console.log("Value of For Loop: " +i);
}

//while loop
let j=1;
while(j<=10){
    console.log("Value of while Loop: " +j);
    j++;
}

//do while
let k=1;
do{
    console.log("Value of do while Loop: " +k);
    k++
}while(k<=10);