//Printing Number from 1 to 10 with all loop in reverse;
//for loop
for( let i=10;i>=1;i--){
    console.log("Value of For Loop: " +i)
}

//while loop
let j=10;
while(j>=1){
    console.log("Value of while Loop: " +j);
    j--;
}

//do while
let k=10;
do{
    console.log("Value of do while Loop: " +k);
    k--
}while(k>=1);